import UIKit

class Calculadora {
    func soma(valor1: Int, valor2: Int) -> Int {
        return valor1 + valor2
    }
    
    func subtracao(valor1: Int, valor2: Int) -> Int {
        return valor1 - valor2
    }
    
    func diferente(valor1: Int, valor2: Int) -> Bool {
        return valor1 != valor2
    }
}


